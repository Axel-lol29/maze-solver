START = (0, 0)  # Coordenadas de inicio
END = (9, 9)    # Coordenadas de fin

def solve_maze_bfs(maze, start, end):
    """Resuelve el laberinto usando el algoritmo de Búsqueda en Amplitud (BFS)."""
    rows, cols = len(maze), len(maze[0])
    queue = collections.deque([(start, [start])])  # Fila de nodos a explorar
    visited = set()  # Conjunto de nodos visitados
    visited.add(start)

    while queue:
        (curr_row, curr_col), path = queue.popleft()  # Obtener el siguiente nodo y su camino

        if (curr_row, curr_col) == end:  # Si llegamos al final, retornamos el camino
            return path

        # Movimientos posibles: arriba, abajo, izquierda, derecha
        for dr, dc in [(0, 1), (0, -1), (1, 0), (-1, 0)]:
            next_row, next_col = curr_row + dr, curr_col + dc

            # Verificamos si la nueva posición es válida
            if 0 <= next_row < rows and 0 <= next_col < cols and \
               maze[next_row][next_col] == 0 and (next_row, next_col) not in visited:
                visited.add((next_row, next_col))  # Marcamos como visitado
                new_path = list(path)  # Copiar el camino
                new_path.append((next_row, next_col))  # Agregar el nuevo nodo al camino
                queue.append(((next_row, next_col), new_path))  # Añadir al final de la cola

    return None  # Si no se encontró camino

